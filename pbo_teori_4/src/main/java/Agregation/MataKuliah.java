package Agregation;

public class MataKuliah {
     private String Mk1;
    private String Mk2;
    private String Mk3;
    private String Mk4;

    public MataKuliah (String Mk1, String Mk2, String Mk3, String Mk4) {
        this.Mk1 = Mk1;
        this.Mk2 = Mk2;
        this.Mk3 = Mk3;
        this.Mk4 = Mk4;
    }

    public String Mk1() {
        return Mk1;
    }

    public String Mk2() {
        return Mk2;
    }

    public String Mk3() {
        return Mk3;
    }

    public String Mk4() {
        return Mk4;
    }

    
}

